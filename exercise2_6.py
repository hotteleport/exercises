import math
# calculations
cherry = 2 + 2 * 2 + 2 - 2 - 2
apple = math.sqrt(3 + 10 - 4) / 3
apple = apple + (5 ** 3 - 5) / 20
apple = apple + 3

# fruits calculations
orange = apple - 9
banana = cherry - 10
pear = banana - 8

# final calculation
total = (apple - banana + orange) * (pear + cherry)
print(total)